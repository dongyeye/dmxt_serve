package fzu.edu.dmxt.pojo;

public class Registerdetail {

    private int id;
    private int register_ID;
    private int course_ID;
    private int sid;
    private int mark;


    private int goaway;


    private String student_name;
    private String student_profession;



    private String student_ID;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRegister_ID() {
        return register_ID;
    }

    public void setRegister_ID(int register_ID) {
        this.register_ID = register_ID;
    }

    public int getCourse_ID() {
        return course_ID;
    }

    public void setCourse_ID(int course_ID) {
        this.course_ID = course_ID;
    }

    public int getSid() {
        return sid;
    }

    public void setSid(int sid) {
        this.sid = sid;
    }

    public int getMark() {
        return mark;
    }

    public void setMark(int mark) {
        this.mark = mark;
    }

    public int getGoaway() {
        return goaway;
    }

    public void setGoaway(int goaway) {
        this.goaway = goaway;
    }


    public String getStudent_name() {
        return student_name;
    }

    public void setStudent_name(String student_name) {
        this.student_name = student_name;
    }

    public String getStudent_profession() {
        return student_profession;
    }

    public void setStudent_profession(String student_profession) {
        this.student_profession = student_profession;
    }

    public String getStudent_ID() {
        return student_ID;
    }

    public void setStudent_ID(String student_ID) {
        this.student_ID = student_ID;
    }


}
